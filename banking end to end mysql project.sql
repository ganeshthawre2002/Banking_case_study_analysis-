    `client_id` VARCHAR(8),
    `Name` VARCHAR(21),
    `Age` INT,
    `Location_ID` INT,
    `Joined_Bank` VARCHAR(10),
    `Banking_Contact` VARCHAR(19),
    `Nationality` VARCHAR(10),
    `Occupation` VARCHAR(36),
    `Fee_Structure` VARCHAR(4),
