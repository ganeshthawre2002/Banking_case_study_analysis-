    `Estimated_Income` DOUBLE,
    `Superannuation_Savings` DOUBLE,
    `Amount_of_Credit_Cards` INT,
    `Credit_Card_Balance` DOUBLE,
    `Bank_Loans` DOUBLE,
    `Bank_Deposits` DOUBLE,
    `Checking_Accounts` DOUBLE,
    `Saving_Accounts` DOUBLE,
    `Foreign_Currency_Account` DOUBLE,
    `Business_Lending` DOUBLE,
    `Properties_Owned` INT,
    `Risk_Weighting` INT,
